package com.rays.basicPrograms;

public class TestSwitchStatement {

	public static void main(String[] args) {
		int number = 1;
		switch (number) {
		case 1:
			System.out.println("One");
			break;

		case 2:
			System.out.println("Two");
			break;
			
		default:
			System.out.println("Other number");
		}
	}
	
}
